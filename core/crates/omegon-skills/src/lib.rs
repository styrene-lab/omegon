    #[test]
    fn manifest_to_skill_file_renders_yaml_frontmatter() {
        let manifest = SkillManifest {
            name: "test".into(),
            description: "Test skill".into(),
            ..Default::default()
        };
        let file = manifest.to_skill_file("# My Skill\n\nDo things.\n");
        assert!(file.starts_with("---"));
        assert!(file.contains("name: test"));
        assert!(file.contains("# My Skill"));
    }

    #[test]
    fn parse_skill_file_yaml() {
        let content = "---\nname: test\ndescription: A test\nactivation: project_detected\nprofile: [coding]\nproject_signals: [Cargo.toml]\n---\n\n# Body\n";
        let (manifest, body) = parse_skill_file(content);
        assert_eq!(manifest.name, "test");
        assert_eq!(manifest.description, "A test");
        assert_eq!(manifest.activation, Some("project_detected".into()));
        assert_eq!(manifest.profile, vec!["coding"]);
        assert_eq!(manifest.project_signals, vec!["Cargo.toml"]);
        assert!(body.contains("# Body"));
    }

    #[test]
    fn parse_skill_file_toml() {
        let content = "+++\nname = \"test\"\ndescription = \"A test\"\nversion = \"1.0.0\"\nactivation = \"project_detected\"\nprofile = [\"coding\"]\nproject_signals = [\"Cargo.toml\"]\n+++\n\n# Body\n\nDo things.\n";
        let (manifest, body) = parse_skill_file(content);
        assert_eq!(manifest.name, "test");
        assert_eq!(manifest.description, "A test");
        assert_eq!(manifest.version, Some("1.0.0".into()));
        assert_eq!(manifest.activation, Some("project_detected".into()));
        assert_eq!(manifest.profile, vec!["coding"]);
        assert_eq!(manifest.project_signals, vec!["Cargo.toml"]);
        assert!(body.contains("# Body"));
    }

    #[test]
    fn parse_skill_file_no_frontmatter() {
        let content = "# Just markdown\n\nNo frontmatter here.\n";
        let (manifest, body) = parse_skill_file(content);
        assert_eq!(manifest.name, "");
        assert!(body.contains("Just markdown"));
    }

    #[test]
    fn split_frontmatter_detects_toml_yaml_and_absence() {
        let (fm, body) = split_frontmatter("+++\nname = \"test\"\n+++\n\nBody here.\n");
        assert!(fm.is_some());
        let (format, text) = fm.unwrap();
        assert_eq!(format, FrontmatterFormat::Toml);
        assert!(text.contains("name = \"test\""));
        assert!(body.contains("Body here."));

        let (fm, body) = split_frontmatter("---\nname: test\n---\n\nBody here.\n");
        assert!(fm.is_some());
        let (format, text) = fm.unwrap();
        assert_eq!(format, FrontmatterFormat::Yaml);
        assert!(text.contains("name: test"));
        assert!(body.contains("Body here."));

        let content = "No delimiters here.\n";
        let (fm, body) = split_frontmatter(content);
        assert!(fm.is_none());
        assert_eq!(body, content);
    }

    #[test]
    fn activation_metadata_diagnostics_cover_invalid_and_unsafe_shapes() {
        let invalid = SkillManifest {
            name: "custom".into(),
            description: "Custom skill".into(),
            activation: Some("projectish".into()),
            profile: vec!["codign".into()],
            ..Default::default()
        };
        let diagnostics = validate_activation_metadata(&invalid);
        assert!(diagnostics.activation.is_none());
        assert!(
            diagnostics
                .warnings
                .iter()
                .any(|w| w.contains("unknown activation 'projectish'"))
        );
        assert!(
            diagnostics
                .warnings
                .iter()
                .any(|w| w.contains("unknown profile 'codign'"))
        );

        let project_without_signals = SkillManifest {
            name: "custom".into(),
            description: "Custom skill".into(),
            activation: Some("project_detected".into()),
            profile: vec!["coding".into()],
            ..Default::default()
        };
        let diagnostics = validate_activation_metadata(&project_without_signals);
        assert_eq!(
            diagnostics.activation,
            Some(SkillActivation::ProjectDetected)
        );
        assert!(diagnostics.profiles.contains(&SkillProfile::Coding));
        assert!(
            diagnostics
                .warnings
                .iter()
                .any(|w| w.contains("no project_signals"))
        );

        let global_always = SkillManifest {
            name: "custom".into(),
            description: "Custom skill".into(),
            activation: Some("always".into()),
            ..Default::default()
        };
        let diagnostics = validate_activation_metadata(&global_always);
        assert_eq!(diagnostics.activation, Some(SkillActivation::Always));
        assert!(
            diagnostics
                .warnings
                .iter()
                .any(|w| w.contains("profile-scoped"))
        );
    }

    #[test]
    fn project_signal_matching_supports_literals_root_globs_and_recursive_globs() {
        let dir = tempfile::tempdir().unwrap();
        let root = dir.path();
        std::fs::write(root.join("Cargo.toml"), "").unwrap();
        std::fs::write(root.join("main.rs"), "").unwrap();
        std::fs::create_dir_all(root.join("docs/nested")).unwrap();
        std::fs::write(root.join("docs/nested/guide.md"), "").unwrap();
        std::fs::create_dir_all(root.join("target/generated")).unwrap();
        std::fs::write(root.join("target/generated/ignored.md"), "").unwrap();

        let cargo = match_project_signal(root, "Cargo.toml").unwrap().unwrap();
        assert_eq!(cargo.kind, SkillSignalKind::Literal);
        assert_eq!(cargo.matched_path, "Cargo.toml");

        let rust = match_project_signal(root, "*.rs").unwrap().unwrap();
        assert_eq!(rust.kind, SkillSignalKind::RootGlob);
        assert_eq!(rust.matched_path, "main.rs");

        let docs = match_project_signal(root, "docs/**/*.md").unwrap().unwrap();
        assert_eq!(docs.kind, SkillSignalKind::RecursiveGlob);
        assert_eq!(docs.matched_path, "docs/nested/guide.md");

        assert!(
            match_project_signal(root, "target/**/*.md")
                .unwrap()
                .is_none()
        );
    }

    #[test]
    fn project_signal_validation_rejects_traversal_and_broad_recursive_shapes() {
        for signal in [
            "",
            "/tmp/file",
            "../Cargo.toml",
            "foo/../bar",
            "foo//bar",
            "foo\\bar",
            "**",
            "foo/**/bar/**/baz",
        ] {
            assert!(
                validate_project_signal(signal).is_err(),
                "{signal} should be rejected"
            );
        }
    }

    #[test]
    fn adapted_skill_warnings_cover_provenance_and_executable_posture() {
        assert!(adapted_skill_warnings("# Normal Skill\n\nNo upstream provenance.").is_empty());

        let missing = adapted_skill_warnings("## Provenance\nSource: upstream\n");
        assert!(
            missing.iter().any(|w| w.contains("## Adaptation Notes")),
            "{missing:?}"
        );
        assert!(
            missing.iter().any(|w| w.contains("## Safety Notes")),
            "{missing:?}"
        );

        let clean_missing = adapted_skill_warnings(
            "## Provenance\nKind: clean-room adaptation\n## Adaptation Notes\nNotes\n## Safety Notes\nSafe\n",
        );
        assert!(
            clean_missing
                .iter()
                .any(|w| w.contains("## Omitted Upstream Assets")),
            "{clean_missing:?}"
        );
        assert!(
            clean_missing
                .iter()
                .any(|w| w.contains("## Clean-room Rewrite Notes")),
            "{clean_missing:?}"
        );

        let executable_missing = adapted_skill_warnings("Run npm install from package.json");
        assert!(
            executable_missing
                .iter()
                .any(|w| w.contains("without explicit adaptation trust posture")),
            "{executable_missing:?}"
        );

        let complete_clean = adapted_skill_warnings(
            "## Provenance\nKind: clean-room adaptation\n## Adaptation Notes\nNotes\n## Safety Notes\nSafe\n## Omitted Upstream Assets\n- package.json\n## Clean-room Rewrite Notes\nRewritten without npm.\n",
        );
        assert!(complete_clean.is_empty(), "{complete_clean:?}");

        let trusted = adapted_skill_warnings(
            "## Provenance\nKind: trusted executable import\n## Adaptation Notes\nNotes\n## Safety Notes\nOperator trusts package.json.\nRun npm install.\n",
        );
        assert!(trusted.is_empty(), "{trusted:?}");
    }
