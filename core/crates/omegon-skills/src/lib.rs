    #[test]
    fn bundle_discovery_reports_missing_script_refs_and_ignores_vendor_dirs() {
        let dir = tempfile::tempdir().unwrap();
        let skill_dir = dir.path().join("skill-a");
        std::fs::create_dir_all(&skill_dir).unwrap();
        std::fs::write(
            skill_dir.join("SKILL.md"),
            "---\nname: skill-a\ndescription: Skill A\n---\n\nUse scripts/missing.py\n",
        )
        .unwrap();
        std::fs::create_dir_all(dir.path().join("node_modules/ignored-skill")).unwrap();
        std::fs::write(
            dir.path().join("node_modules/ignored-skill/SKILL.md"),
            "---\nname: ignored\ndescription: Ignored\n---\n\nBody\n",
        )
        .unwrap();

        let bundles = discover_skill_bundles("test", dir.path()).unwrap();
        assert_eq!(bundles.len(), 1);
        assert_eq!(bundles[0].name, "skill-a");
        assert_eq!(bundles[0].missing_script_refs, vec!["scripts/missing.py"]);
    }

    #[test]
    fn imported_skill_summary_lists_resources_and_conflicts() {
        let dir = tempfile::tempdir().unwrap();
        let root = dir.path();
        std::fs::create_dir_all(root.join("scripts/nested")).unwrap();
        std::fs::create_dir_all(root.join("resources/templates")).unwrap();
        std::fs::write(
            root.join("SKILL.md"),
            "---\nname: rust-helper\ndescription: Rust helper\ntriggers: [rust]\n---\n\nBody\n",
        )
        .unwrap();
        std::fs::write(root.join("scripts/run.sh"), "echo run\n").unwrap();
        std::fs::write(root.join("scripts/nested/check.py"), "print('ok')\n").unwrap();
        std::fs::write(root.join("resources/templates/readme.md"), "template\n").unwrap();
        let existing = vec![SkillEntry {
            name: "other-rust".into(),
            description: String::new(),
            id: None,
            version: None,
            tags: Vec::new(),
            aliases: Vec::new(),
            triggers: vec!["rust".into()],
            activation: None,
            profile: Vec::new(),
            project_signals: Vec::new(),
            posture: None,
            max_turns: None,
            installed: true,
            bundled: false,
            project_local: false,
            source: "user".into(),
            editable: true,
            reloadable: true,
            shadows: Vec::new(),
            conflicts: Vec::new(),
            path: String::new(),
        }];

        let summary = summarize_imported_skill(root, "rust-helper", &existing);
        assert_eq!(
            summary.scripts,
            vec![
                "scripts/nested/check.py".to_string(),
                "scripts/run.sh".to_string(),
            ]
        );
        assert_eq!(
            summary.resources,
            vec!["resources/templates/readme.md".to_string()]
        );
        assert_eq!(summary.conflicts, vec!["user/other-rust".to_string()]);
    }
